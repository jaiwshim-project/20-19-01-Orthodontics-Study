    if (!modal) {
        console.error('resourceModal not found');
        return;
    }

    const content = document.getElementById('resourceDetail');

    // 슬라이드 타입이면 요약 슬라이드 생성
    if (type === 'slides') {
        currentSummarySlides = generateSummarySlidesForSlide(resource.id, resource.title);
        currentSummaryIndex = 0;
        showSummarySlide();
        modal.classList.add('show');
        return;
    }

